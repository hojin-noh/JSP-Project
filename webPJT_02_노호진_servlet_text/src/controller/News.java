import dao.News_dao;
import dto.News_dto;

/**
 * Servlet implementation class News
 */
@WebServlet("/News")
public class News extends HttpServlet {
	private static final long serialVersionUID = 1L;
       
    /**
     * @see HttpServlet#HttpServlet()
     */
    public News() {
        super();
        // TODO Auto-generated constructor stub
    }

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		//response.getWriter().append("Served at: ").append(request.getContextPath());
		request.setCharacterEncoding("utf-8");
		News_dao dao = new News_dao();
		
		String select = request.getParameter("t_select");
		String search = request.getParameter("t_search");
		if(select == null) {
			select = "title";
			search = "";
		}
		
//		ArrayList<News_dto> arr = dao.getNewsView(select, search);
		ArrayList<News_dto> arr = dao.getNewsView();
		
/*		String name = "홍길동";
		String area = "대전";
		request.setAttribute("t_name", name);
		request.setAttribute("t_area", area);
*/
		request.setAttribute("t_arr", arr);
		
		
		RequestDispatcher dispatcher = request.getRequestDispatcher("/news/news_list.jsp");
		dispatcher.forward(request, response);
		
	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		doGet(request, response);
	}

}
